public class ConditionalStatement {
    public static void main(String[] args) {
        int x = 5;
        if (x > 2) {
            System.out.println("Wartosc zmiennej x jest wieksze od 2");
        } else if (x == 2) {
            System.out.println("Wartość zmiennej x jest rowna 2");
        } else {
            System.out.println("Wartosc zmiennej x jest mniejsza od 2");
        }

    }
}
