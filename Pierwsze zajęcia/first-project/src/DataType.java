public class DataType {
    public static void main(String[] args) {
        // w cudzysłowach zapisujemy tekst
        //liczby całkowite zapisujemy bezposrednio
        System.out.println("tekst");
        //
        System.out.println(123);
        // operator "+" będzie operatorem konkatencji (sklejania tekstów)
        //gdy co najmniej jeden z argumentow jest tekstem
        System.out.println("123" + "456");
        System.out.println(123 + 456);
        System.out.println(3*4);
        System.out.println(8/4);
        // operator reszty z dzielenia (modulo) "%"
        System.out.println(9%4);
    }
}
